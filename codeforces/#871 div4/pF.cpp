#define faster ios_base::sync_with_stdio(false);cin.tie(0);
#define pii pair<int, int>
#define vi vector<int>
#define ff first
#define ss second
typedef long long ll;
using namespace std;
int main(){
    faster;
    int t;
    cin>>t;
    while(t--){
    	int ver,edge;
    	cin>>ver>>edge;
    	vector<vector<int>>adj(ver+5);
    	int cnt[ver+5];
    	for(int i=0;i<ver+5;i++)cnt[i]=0;
    	for(int i=0;i<edge;i++){
    		int a,b;
    		cin>>a>>b;
    		cnt[a]++;
    		cnt[b]++;
    		adj[a].push_back(b);
    		adj[b].push_back(a);
		}
		int start=0;
		int mid=0;
		int core=0;
		for(int i=1;i<=ver;i++){
			if(cnt[i]==1){
				start=i;
				break;
			}
		}
		//for(int i=1;i<=ver;i++)cout<<cnt[i]<<' ';
		//cout<<'\n';
		//cout<<start<<'\n';
		mid=adj[start][0];
		//cout<<mid<<'\n';
		vector<int>temp=adj[mid];
		for(int i=0;i<temp.size();i++){
			if(cnt[temp[i]]!=1)core=temp[i];
		}
		//cout<<core<<'\n';
		if(core==0){
			cout<<1<<' '<<cnt[mid]-1<<'\n';
		}
		else cout<<cnt[core]<<' '<<cnt[mid]-1<<'\n';
	}
}